namespace Blazor.Lottie.Player;
/// <summary>
/// Specifies the playback direction for a Lottie animation.
/// </summary>
public enum LottieAnimationDirection
{
    /// <summary>
    /// Represents the forward direction an animation will play.
    /// </summary>
    Forward = 1,
    /// <summary>
    /// Represents the reverse direction an animation will play.
    /// </summary>
    Reverse = -1,
}
